// --- 중복 제거 → 1000개 ---
const seen = new Set()
const out = []
for (const q of pool) {
  if (seen.has(q.body)) continue
  seen.add(q.body)
  out.push(q)
}
if (out.length < 1000) throw new Error(`부족: ${out.length}개 (1000 필요)`)

const final = out.slice(0, 1000).map((q, i) => ({
  id: i + 1,
  body: q.body,
  source: q.source,
  license: q.license,
}))

mkdirSync('data', { recursive: true })
const json = '[\n' + final.map((o) => '  ' + JSON.stringify(o)).join(',\n') + '\n]\n'
writeFileSync('data/quotes.seed.json', json)
console.log(`생성: 총 후보 ${pool.length}, 고유 ${out.length}, 저장 ${final.length}`)
